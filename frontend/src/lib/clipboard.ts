/**
 * Copy text to the clipboard. Returns true on success.
 *
 * The async Clipboard API only exists in secure contexts (HTTPS or localhost),
 * so over plain HTTP `navigator.clipboard` is undefined. Fall back to a hidden
 * textarea + execCommand("copy") so the copy still works on those origins.
 */
export async function copyText(text: string): Promise<boolean> {
  if (navigator.clipboard && window.isSecureContext) {
    try {
      await navigator.clipboard.writeText(text);
      return true;
    } catch {
      // fall through to the legacy path
    }
  }

  try {
    const ta = document.createElement("textarea");
    ta.value = text;
    ta.setAttribute("readonly", "");
    ta.style.position = "fixed";
    ta.style.top = "-9999px";
    document.body.appendChild(ta);
    ta.select();
    const ok = document.execCommand("copy");
    ta.remove();
    return ok;
  } catch {
    return false;
  }
}
